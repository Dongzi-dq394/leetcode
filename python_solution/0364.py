class Solution:
    def depthSumInverse(self, nestedList: List[NestedInteger]) -> int:
        # Solution 1: Recursion (32ms: 65.15%)
        dic = defaultdict(list)
        def helper(nested, level):
            for item in nested:
                if item.isInteger():
                    dic[level].append(item.getInteger())
                else:
                    helper(item.getList(), level-1)
        helper(nestedList, -1)
        if not dic: return 0
        res = 0
        diff = abs(min(list(dic.keys())))+1
        for key in dic:
            for num in dic[key]:
                res += (key+diff)*num
        return res